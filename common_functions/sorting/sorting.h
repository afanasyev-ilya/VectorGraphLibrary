#pragma once

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifdef __USE_ASL__
#include <asl.h>
#endif

#include <math.h>
#include <algorithm>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Sorting
{
public:
    Sorting() {};
    
    void sort_array(double *_array, int _size)
    {
        #ifdef __USE_ASL__
        double *wk = new double[_size];
        asl_int_t *iwk = new asl_int_t[1];
        ASL_dssta1(_array, _size, 3, wk, iwk);
        delete []iwk;
        delete []wk;
        #else
        sort(_array, _array + _size);
        #endif
    }
    
    ~Sorting() {};
};

